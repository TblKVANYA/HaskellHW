part :: Int -> Int -> [[Int]]
part n m 
    | (n < 0) || (m <= 0) = error "incorrect values"
    | n == 0 = [replicate m 0]
    | m == 1 = [[n]]
    | otherwise = concat[map ([k]++) (part (n-k) (m-1))| k <- [0..n]]

