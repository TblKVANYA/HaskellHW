
segsSetLen :: Int -> [a] -> [[a]]
segsSetLen n l 
    | length l == n = [l]
    | otherwise =  take n l : segsSetLen n (tail l)


segs :: [a] -> [[a]]
segs l = concat [segsSetLen n l | n <- [1..(length l)]]


